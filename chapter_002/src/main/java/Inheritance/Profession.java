package Inheritance;

public class Profession {

    String name;
    String surname;
    String education;
    String birthday;

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getEducation() {
        return education;
    }

    public String getBirthday() {
        return birthday;
    }



}
